export const STRINGS: Record<Lang, typeof en> = { en, bn };
export type StringKey = keyof typeof en;

const BN_DIGITS: Record<string, string> = {
  "0": "০", "1": "১", "2": "২", "3": "৩", "4": "৪",
  "5": "৫", "6": "৬", "7": "৭", "8": "৮", "9": "৯",
};
export function bnNum(v: string | number): string {
  return String(v).replace(/[0-9]/g, (d) => BN_DIGITS[d]);
}
